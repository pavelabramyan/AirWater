window.currentLang = 'ru';
let currentLang = 'ru'; // Keep for backward compatibility

function setLanguage(lang) {
    window.currentLang = lang;
    currentLang = lang; // Keep for backward compatibility
    document.documentElement.lang = lang;
    
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            el.innerHTML = translations[lang][key];
        }
    });
    
    // Update placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[lang] && translations[lang][key]) {
            el.placeholder = translations[lang][key];
        }
    });
    
    // Update select option
    document.getElementById('langSelect').value = lang;
    
    // Recalculate calculator with new language
    if (typeof calculateROI === 'function') {
        calculateROI();
    }
}

// Initialize language on load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('preferredLang') || 'ru';
    setLanguage(savedLang);
    
    // Language selector change handler
    document.getElementById('langSelect').addEventListener('change', (e) => {
        setLanguage(e.target.value);
        localStorage.setItem('preferredLang', e.target.value);
    });
});
